高层管理者，把握企业文化发展的方向；中层管理者，根据公司导向着力建设组织文化；基层管理者搞好团队建设，起到“传、帮、带”作用；普通员工，立足岗位做好本职工作；新员工，通过“见、修、行”三个阶段融入企业文化。
